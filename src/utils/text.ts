
/**
 * Очищает заголовок темы теста от служебного суффикса-типа тестирования,
 * сохраняя год.
 *
 * Модели НМО показывают темы в трёх формах: «Тема», «Тема - 2024»,
 * «Тема - 2024 - Предварительное/Контрольное/Итоговое тестирование».
 * Для поиска на сайтах-источниках тип тестирования лишний — он мешает
 * совпадению, потому что на источниках его нет.
 *
 * Примеры:
 * - `"X - 2024 - Предварительное тестирование"` → `"X - 2024"`
 * - `"X - Контрольное тестирование"`            → `"X"`
 * - `"X - 2024"`                                 → `"X - 2024"` (без изменений)
 * - `"X"`                                        → `"X"` (без изменений)
 *
 * @param topic Заголовок из DOM. `null` пробрасывается как есть, чтобы не было
 *              ветвления на стороне вызывающего.
 * @returns Очищенная тема или `null`, если на входе был `null`/пустая строка.
 */
export function cleanTopic(topic: string | null): string | null {
	if (!topic) return null;
	return topic
		.replace(/\s*-\s*(\d{4})\s*-\s*(?:Предварительное|Контрольное|Итоговое)[\s\S]*$/i, ' - $1')
		.replace(/\s*-\s*(?:Предварительное|Контрольное|Итоговое)[\s\S]*$/i, '')
		.trim();
}

/**
 * Очищает текст ответа от мусора парсинга:
 *  - любые пробельные серии (включая `\n`, `\t`, `&nbsp;`) → один пробел;
 *  - завершающие `;`, `+`, `.`;
 *  - ведущая нумерация вида `N)`.
 *
 * Схлопывание пробелов нужно, потому что в jsdom-тестах `innerText`
 * замещается `textContent` и не нормализует whitespace как реальный
 * браузер. Страхуемся и на проде — страницы источников иногда
 * содержат `&nbsp;` / отступы посреди ответа.
 *
 * @param text Сырой текст варианта ответа из DOM источника.
 * @returns Очищенный текст, готовый к сравнению.
 */
export function cleanAnswer(text: string): string {
	return text
		.replace(/\s+/g, ' ')
		.replace(/[\s;+.]+$/, '')
		.replace(/^\d+\)\s*/, '')
		.trim();
}

/**
 * Все виды кавычек: «», “”, „”, ‘’, ‚‛, ASCII `"` и `'`.
 * Используются `stripQuotes` для вырезания, а не замены — чтобы не генерить
 * мусорные биграммы в {@link similarity}.
 */
const QUOTE_RE = /[\u00AB\u00BB\u201C\u201D\u201E\u201F\u2018\u2019\u201A\u201B"']/g;

/**
 * Вырезает из строки все виды кавычек.
 *
 * Не заменяет на единый вид, а именно удаляет: если оставить один тип,
 * в биграммах появятся лишние пары, и similarity будет ниже реального сходства.
 * Используется в связке с {@link normalizeDashes} при матчинге коротких
 * вариантов ответа, где «Критик» и "Критик" должны считаться идентичными.
 *
 * @param s Произвольная строка.
 * @returns Строка без кавычек.
 */
export function stripQuotes(s: string): string {
	return s.replace(QUOTE_RE, '');
}

/**
 * Все Unicode-варианты тире и дефисов, включая soft hyphen и fullwidth-дефис.
 * Мапятся на обычный ASCII `-`.
 */
const DASH_RE = /[\u2010\u2011\u2012\u2013\u2014\u2015\u2212\u00AD\uFE58\uFE63\uFF0D]/g;

/**
 * Универсальная нормализация для fuzzy-сравнения строк: тире, пробелы,
 * кириллица-двойники → латиница, trim, lowercase.
 *
 * Название историческое — изначально функция только мапила тире, потом
 * обросла остальными шагами. По сути эквивалентна {@link normalizeText}.
 *
 * Шаги по порядку:
 *  1. Все виды тире/дефисов (см. {@link DASH_RE}) → ASCII `-`.
 *  2. Любые последовательности пробельных символов → один пробел.
 *  3. Кириллические омоглифы `А Е О Р С Х` (визуально неотличимые от латинских
 *     `A E O P C X`) → соответствующие латинские. Без этого «Антибиотик» на
 *     одном сайте и «Aнтибиотик» (латинская A) на другом не совпадут.
 *  4. `trim()` + `toLowerCase()`.
 *
 * @param s Произвольная строка.
 * @returns Нормализованная строка, готовая к сравнению через `===`, `includes`
 *          или {@link similarity}.
 */
export function normalizeDashes(s: string): string {
	return s
		.replace(DASH_RE, '-')
		.replace(/\s+/g, ' ')
		.replace(/\u0410/gi, 'a').replace(/\u0415/gi, 'e')
		.replace(/\u041E/gi, 'o').replace(/\u0420/gi, 'p')
		.replace(/\u0421/gi, 'c').replace(/\u0425/gi, 'x')
		.trim()
		.toLowerCase();
}

/**
 * Нормализация для точного сравнения (`===`) текстов вариантов ответа.
 *
 * По итоговому результату эквивалентна {@link normalizeDashes} — обе возвращают
 * lowercase-строку с латинскими омоглифами, нормализованными пробелами и тире.
 * Историческое дублирование: `normalizeText` выделен отдельно для модуля
 * `matching.ts` (сопоставление вариантов), тогда как `normalizeDashes`
 * используется парсерами для вопроса.
 *
 * Отличие в реализации: здесь кириллица мапится парами (верх→верх, низ→низ),
 * а затем `toLowerCase()` уже в конце; в `normalizeDashes` используется
 * case-insensitive regex. Финальный вывод один и тот же.
 *
 * @param s Произвольная строка.
 * @returns Нормализованная строка для сравнения через `===`.
 */
export function normalizeText(s: string): string {
	return s
		.replace(DASH_RE, '-')
		.replace(/\s+/g, ' ')
		.replace(/\u0410/g, 'A').replace(/\u0430/g, 'a')
		.replace(/\u0415/g, 'E').replace(/\u0435/g, 'e')
		.replace(/\u041E/g, 'O').replace(/\u043E/g, 'o')
		.replace(/\u0420/g, 'P').replace(/\u0440/g, 'p')
		.replace(/\u0421/g, 'C').replace(/\u0441/g, 'c')
		.replace(/\u0425/g, 'X').replace(/\u0445/g, 'x')
		.trim()
		.toLowerCase();
}
